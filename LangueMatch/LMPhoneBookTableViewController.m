#import "LMPhoneBookTableViewController.h"
#import "LMPerson.h"

@interface LMPhoneBookTableViewController ()

@property (copy, nonatomic) NSArray *contactList;

@end

@implementation LMPhoneBookTableViewController

static NSString *const reuseIdentifier = @"cell";

-(instancetype) initWithContactList:(NSArray *)contacts
{
    if (self = [super initWithStyle:UITableViewStylePlain]) {
        _contactList = contacts;
    }
    return self;
}

- (void)viewDidLoad {
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
    
    [self.tableView registerClass:[UITableViewCell class] forCellReuseIdentifier:reuseIdentifier];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return _contactList.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:reuseIdentifier forIndexPath:indexPath];
    
    if (!cell) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:reuseIdentifier];
    }
    
    LMPerson *person = _contactList[indexPath.row];
    
    [cell.textLabel setText:person.fullName];
    [cell.detailTextLabel setText:person.homeEmail];
    
    if (person.image != nil) {
        cell.imageView.image = person.image;
    } else {
        cell.imageView.image = [UIImage imageNamed:@"empty_profile.png"];
    }
    
    return cell;
}


